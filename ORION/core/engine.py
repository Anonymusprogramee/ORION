from core.registry import PluginRegistry
from core.logger import OrionLogger

class OrionEngine:
    def __init__(self):
        self.registry = PluginRegistry()
        self.logger = OrionLogger()
        self.logger.info("ORION Engine initialized")

    def load_plugins(self):
        self.registry.discover_plugins()
        self.logger.info(f"Loaded {len(self.registry.plugins)} plugins")

    def run_all(self):
        for plugin in self.registry.plugins:
            self.logger.info(f"Running plugin: {plugin.name}")
            plugin.run()
