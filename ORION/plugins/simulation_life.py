import random
from core.plugin import OrionPlugin

class LifeSimulation(OrionPlugin):
    name = "Life Simulation"
    version = "1.0"

    def run(self):
        grid = [[random.choice([0, 1]) for _ in range(10)] for _ in range(10)]
        alive = sum(sum(row) for row in grid)
        print(f"[LifeSimulation] Alive cells: {alive}")
